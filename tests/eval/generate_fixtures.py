#!/usr/bin/env python3
"""Generate eval fixture pairs (input.ipynb + expected.ipynb).

Each case function returns an input notebook. The expected notebook is
generated by running the formatter on a copy of the input. This keeps
fixtures in sync with actual formatter behavior.

Usage:
    python tests/eval/generate_fixtures.py
"""

import shutil
import sys
from pathlib import Path

# Add project root to path before importing sqlnbfmt
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

import nbformat  # noqa: E402
from nbformat import v4 as nbf  # noqa: E402

from sqlnbfmt.formatter import load_config, process_notebook, setup_logging  # noqa: E402

EVAL_DIR = Path(__file__).resolve().parent
DIALECT = "mysql"


# ---------------------------------------------------------------------------
# SQL Pattern Breadth
# ---------------------------------------------------------------------------


def basic_select():
    """Simple SELECT/WHERE/ORDER BY."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell(
            'query = "select id, name from users where active = 1 order by name"'
        ),
    ]
    return nb


def joins():
    """INNER JOIN, LEFT JOIN — multi-table queries."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell(
            'query = "select u.id, u.name, o.total from users u inner join orders o on u.id = o.user_id left join addresses a on u.id = a.user_id"'
        ),
    ]
    return nb


def cte():
    """WITH ... AS common table expression."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell(
            'query = "with active_users as (select id, name from users where active = 1) select * from active_users"'
        ),
    ]
    return nb


def subquery():
    """Nested SELECT in WHERE clause."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell(
            'query = "select * from users where id in (select user_id from orders where total > 100)"'
        ),
    ]
    return nb


def window_function():
    """ROW_NUMBER, RANK OVER() window functions."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell(
            'query = "select id, name, row_number() over (partition by department order by salary desc) as rn from employees"'
        ),
    ]
    return nb


def case_expression():
    """CASE WHEN...THEN...ELSE...END conditional expressions."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell(
            '''query = "select id, case when status = 'active' then 1 when status = 'inactive' then 0 else -1 end as status_flag from users"'''
        ),
    ]
    return nb


def union():
    """UNION ALL between SELECTs."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell(
            'query = "select id, name from users where active = 1 union all select id, name from archived_users where active = 1"'
        ),
    ]
    return nb


def group_by_having():
    """GROUP BY + HAVING + aggregates."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell(
            'query = "select department, count(*) as cnt, avg(salary) as avg_salary from employees group by department having count(*) > 5 order by avg_salary desc"'
        ),
    ]
    return nb


# ---------------------------------------------------------------------------
# Notebook Scenarios
# ---------------------------------------------------------------------------


def comment_preservation():
    """Python comments + SQL in same cell — comments must survive."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell(
            "# Fetch active users for reporting\n"
            "# This is used in the dashboard\n"
            'query = "select id, name, email from users where active = 1"'
        ),
    ]
    return nb


def multi_cell_mixed():
    """SQL cells + non-SQL cells + markdown — only SQL cells touched."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_markdown_cell("# Data Analysis\nThis notebook analyzes user data."),
        nbf.new_code_cell("import pandas as pd\nimport numpy as np"),
        nbf.new_code_cell('query = "select id, name from users where active = 1"'),
        nbf.new_code_cell("df = pd.DataFrame({'a': [1, 2, 3]})"),
        nbf.new_markdown_cell("## Results"),
        nbf.new_code_cell('result = "select count(*) as total from orders"'),
    ]
    return nb


def magic_commands():
    """%sql and %%sql magic command formatting."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell("%sql select * from users where active = 1"),
        nbf.new_code_cell(
            "%%sql\nselect id, name, email from users where active = 1 order by name"
        ),
        nbf.new_code_cell("# Query with comment\n%%sql\nselect count(*) from orders"),
    ]
    return nb


def fstring_variables():
    """F-strings with various placeholder expressions."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell(
            'table = "users"\nquery = f"select * from {table} where active = 1"'
        ),
        nbf.new_code_cell(
            'cols = "id, name"\n'
            'table = "users"\n'
            'query = f"select {cols} from {table} where active = 1"'
        ),
    ]
    return nb


def in_function_sql():
    """SQL inside pd.read_sql() and execute() — indented function-arg formatting."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell(
            'df = pd.read_sql("select id, name from users where active = 1", conn)'
        ),
        nbf.new_code_cell(
            """cursor.execute("select count(*) from orders where status = 'completed'")"""
        ),
    ]
    return nb


def idempotency():
    """Already-formatted notebook — no changes on second run."""
    # This input is already formatted. The expected output should be identical.
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell(
            'query = """\nSELECT\n  id,\n  name\nFROM users\nWHERE\n  active = 1\n"""'
        ),
    ]
    return nb


def non_sql_unchanged():
    """Cells without SQL — zero modifications expected."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell("x = 42\ny = x ** 2\nprint(f'Result: {y}')"),
        nbf.new_code_cell("import os\npath = os.path.join('/tmp', 'data.csv')"),
        nbf.new_code_cell(
            "def fibonacci(n):\n"
            "    a, b = 0, 1\n"
            "    for _ in range(n):\n"
            "        a, b = b, a + b\n"
            "    return a"
        ),
    ]
    return nb


def skip_hint():
    """Cell with # sqlnbfmt: skip — must remain untouched."""
    nb = nbf.new_notebook()
    nb.cells = [
        nbf.new_code_cell(
            '# sqlnbfmt: skip\nquery = "select id, name from users where active = 1"'
        ),
        nbf.new_code_cell(
            'query = "select id, name from users where active = 1"'
        ),
    ]
    return nb


# ---------------------------------------------------------------------------
# Case registry
# ---------------------------------------------------------------------------

CASES = {
    # SQL pattern breadth
    "basic_select": basic_select,
    "joins": joins,
    "cte": cte,
    "subquery": subquery,
    "window_function": window_function,
    "case_expression": case_expression,
    "union": union,
    "group_by_having": group_by_having,
    # Notebook scenarios
    "comment_preservation": comment_preservation,
    "multi_cell_mixed": multi_cell_mixed,
    "magic_commands": magic_commands,
    "fstring_variables": fstring_variables,
    "in_function_sql": in_function_sql,
    "idempotency": idempotency,
    "non_sql_unchanged": non_sql_unchanged,
    "skip_hint": skip_hint,
}


def generate_all():
    """Generate all fixture pairs."""
    config = load_config()
    logger = setup_logging("WARNING")

    for name, factory in CASES.items():
        case_dir = EVAL_DIR / name
        case_dir.mkdir(parents=True, exist_ok=True)

        # Write input notebook
        input_nb = factory()
        input_path = case_dir / "input.ipynb"
        nbformat.write(input_nb, input_path)

        # Generate expected by running the formatter on a copy
        expected_path = case_dir / "expected.ipynb"
        shutil.copy2(input_path, expected_path)
        process_notebook(expected_path, config, DIALECT, logger)

        print(f"  {name}/")

    print(f"\nGenerated {len(CASES)} eval fixture pairs in {EVAL_DIR}")


if __name__ == "__main__":
    generate_all()
